package org.hopen.jjbus.bean;

/**
 * @author jerry
 *
 */
public class JJtime {
	/**
	 * @return the date
	 */
	public String getDate() {
		return date;
	}
	/**
	 * @param date the date to set
	 */
	public void setDate(String date) {
		this.date = date;
	}
	/**
	 * @return the day
	 */
	public String getDay() {
		return day;
	}
	/**
	 * @param day the day to set
	 */
	public void setDay(String day) {
		this.day = day;
	}
	/**
	 * @return the hours
	 */
	public String getHours() {
		return hours;
	}
	/**
	 * @param hours the hours to set
	 */
	public void setHours(String hours) {
		this.hours = hours;
	}
	/**
	 * @return the minutes
	 */
	public String getMinutes() {
		return minutes;
	}
	/**
	 * @param minutes the minutes to set
	 */
	public void setMinutes(String minutes) {
		this.minutes = minutes;
	}
	/**
	 * @return the month
	 */
	public String getMonth() {
		return month;
	}
	/**
	 * @param month the month to set
	 */
	public void setMonth(String month) {
		this.month = month;
	}
	/**
	 * @return the nanos
	 */
	public String getNanos() {
		return nanos;
	}
	/**
	 * @param nanos the nanos to set
	 */
	public void setNanos(String nanos) {
		this.nanos = nanos;
	}
	/**
	 * @return the seconds
	 */
	public String getSeconds() {
		return seconds;
	}
	/**
	 * @param seconds the seconds to set
	 */
	public void setSeconds(String seconds) {
		this.seconds = seconds;
	}
	/**
	 * @return the time
	 */
	public String getTime() {
		return time;
	}
	/**
	 * @param time the time to set
	 */
	public void setTime(String time) {
		this.time = time;
	}
	/**
	 * @return the timezoneOffset
	 */
	public String getTimezoneOffset() {
		return timezoneOffset;
	}
	/**
	 * @param timezoneOffset the timezoneOffset to set
	 */
	public void setTimezoneOffset(String timezoneOffset) {
		this.timezoneOffset = timezoneOffset;
	}
	/**
	 * @return the year
	 */
	public String getYear() {
		return year;
	}
	/**
	 * @param year the year to set
	 */
	public void setYear(String year) {
		this.year = year;
	}
	private String date=null;
	private String day=null;
	private String hours=null;
	private String minutes=null;
	private String month=null;
	private String nanos=null;
	private String seconds=null;
	private String time=null;
	private String timezoneOffset=null;
	private String year=null;
}
